class Repository{
    constructor() {
    }
    async create(data){
        this.create(data);
    }
    async getAll(){
        this.getAll();
    }
    async getById(id){
        this.getById(id);
    }
    async getByEmail(email){
        this.getByEmail(email);
    }
    async update(data){
       this.update(data);
    }
    async delete(id){
        this.delete(id);
    }
}
module.exports = Repository;